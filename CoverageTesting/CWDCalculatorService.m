#import "CWDCalculatorService.h"

@implementation CWDCalculatorService

- (NSInteger)sumOfA:(NSInteger)a andB:(NSInteger)b {
    return a+b;
}

- (NSInteger)productOfA:(NSInteger)a andB:(NSInteger)b {
    return a*b;
}

- (NSInteger)subtractA:(NSInteger)a fromB:(NSInteger)b {
    return b-a;
}

@end
